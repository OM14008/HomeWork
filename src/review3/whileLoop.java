package review3;

public class whileLoop {
    public static void main(String[] args) {

        int time = 9;
        while(time>7) {
            System.out.println("Hi");
            time--;
        }

        System.out.println("Value of time = "+time);

        int a = 10;

        do {
            System.out.println("Bye");
            a--;
        } while(a>6);




    }
}
